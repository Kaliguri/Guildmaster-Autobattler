namespace Guildmaster.Core.Simulation
{
    /// <summary>
    /// Базовый контракт команды симуляции — упорядочивающий ключ для детерминированного
    /// применения на границе тика. Все мутации сим во время боя входят через команды:
    /// это шов host-authoritative мультиплеера и реплеев (см. вики «10. Архитектура» §5.6).
    /// </summary>
    /// <remarks>
    /// Конкретное исполнение (<c>Apply(CombatSimulation)</c>) добавляет слой Combat,
    /// расширяя этот контракт — Core не знает о симуляции и не зависит от неё вверх.
    /// </remarks>
    public interface ISimCommand
    {
        /// <summary>Тик, на границе которого команда применяется.</summary>
        int TargetTick { get; }
    }
}
